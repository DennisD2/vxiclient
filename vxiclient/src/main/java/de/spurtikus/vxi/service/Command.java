package de.spurtikus.vxi.service;

public class Command {
	private String cmd = "a";
	private int value = 11;

	public Command() {
	}

	public String getCmd() {
		return cmd;
	}

	public void setCmd(String cmd) {
		this.cmd = cmd;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
